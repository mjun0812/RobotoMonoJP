def in_ranges(code: int, ranges: tuple[tuple[int, int], ...]) -> bool:
    """Return whether a code point is included in inclusive ranges.

    Args:
        code: Unicode code point.
        ranges: Inclusive code point ranges.

    Returns:
        True when the code point belongs to one of the ranges.
    """
    return any(start <= code <= end for start, end in ranges)
